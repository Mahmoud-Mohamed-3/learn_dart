void main(){
  // Quality and Relational Operators
  // == => equal to
  // != => not equal to
  // > => greater than
  // < => less than
  // >= => greater than or equal to
  // <= => less than or equal to
// all of these operators will return a boolean value (true or false) if you need to store the result use bool type
  int a = 10;
  int b = 5;

  print('Is a equal to b? ${a == b}'); // Output: false
  print('Is a not equal to b? ${a != b}'); // Output: true
  print('Is a greater than b? ${a > b}'); // Output: true
  print('Is a less than b? ${a < b}'); // Output: false
  print('Is a greater than or equal to b? ${a >= b}'); // Output: true
  print('Is a less than or equal to b? ${a <= b}'); // Output: false
}